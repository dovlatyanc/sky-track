export class AINewsService {
  static async generateNews(topic?: string): Promise<{ title: string; content: string }> {
    if (vsegpt) {
      try {
        const prompt = topic
          ? `Напиши короткую новость на тему "${topic}" для авиационного портала. Длина: 200-300 слов. Формат: сначала заголовок, затем текст.`
          : `Напиши актуальную новость из мира авиации. Длина: 200-300 слов. Формат: сначала заголовок, затем текст.`

        const response = await vsegpt.chat.completions.create({
          model: 'gpt-4o',
          messages: [
            { role: 'system', content: 'Ты журналист, пишущий новости об авиации. Пиши на русском.' },
            { role: 'user', content: prompt }
          ],
          temperature: 0.7,
          max_tokens: 1000
        })

        const result = response.choices[0].message.content || ''
        const lines = result.split('\n')
        const title = lines[0].replace(/^#+\s*/, '').trim()
        const content = lines.slice(1).join('\n').trim()
        
        if (title && content) {
          return { title, content }
        }
      } catch (error) {
        console.log('AI API error, using local template')
      }
    }

    // Fallback: используем локальные шаблоны
    if (topic) {
      return {
        title: `Авиановости: ${topic}`,
        content: `Актуальная информация по теме "${topic}". ${templates[0].content}`
      }
    }
    
    const random = templates[Math.floor(Math.random() * templates.length)]
    return random
  }
}